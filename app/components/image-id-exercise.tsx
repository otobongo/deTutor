'use client';

import { useState } from 'react';
import type { ImageAsset } from '@/lib/media/provider';
import type { RecognitionExercise, RecognitionResult } from '@/lib/exercises/image-id';

// Recognition-phase image identification (GT-202). The image arrives as an
// adapter-served asset; grading happens in the caller (which owns corpus
// data and the grammar-log write path) and the result renders warmly here.

export function ImageIdExercise({
  exercise,
  image,
  onChoose,
  result,
}: {
  exercise: RecognitionExercise;
  image: ImageAsset;
  onChoose: (wordId: string) => void;
  result: RecognitionResult | null;
}) {
  const [chosen, setChosen] = useState<string | null>(null);

  return (
    <div className="flex flex-col gap-4" data-testid="image-id-exercise">
      {image.source.type === 'inline-svg' ? (
        <div
          className="max-w-xs"
          data-testid="image-id-image"
          // Placeholder SVGs are generated by our own provider from validated
          // vocabulary data; no untrusted markup enters this path.
          dangerouslySetInnerHTML={{ __html: image.source.svg }}
        />
      ) : (
        /* eslint-disable-next-line @next/next/no-img-element */
        <img src={image.source.url} alt="" className="max-w-xs" data-testid="image-id-image" />
      )}

      <div className="flex flex-col items-start gap-2">
        {exercise.options.map((option) => (
          <button
            key={option.wordId}
            type="button"
            className={`rounded border px-3 py-2 ${chosen === option.wordId ? 'ring-2 ring-blue-700' : ''}`}
            disabled={result !== null}
            onClick={() => {
              setChosen(option.wordId);
              onChoose(option.wordId);
            }}
            data-testid={`image-id-option-${option.wordId}`}
          >
            {option.label}
          </button>
        ))}
      </div>

      {result ? (
        <p data-testid="image-id-feedback" role="status">
          {result.correct
            ? 'Genau! You got it, article and all.'
            : `Not quite. It is ${result.correctLabel}. On we go!`}
        </p>
      ) : null}
    </div>
  );
}
